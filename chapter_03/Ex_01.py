# День недели.

while(True):

    number = int(input("Input number: "))

    if number == 1:
        print("понедельник")
        break
    elif number == 2:
        print("вторник")
        break
    elif number == 3:
        print("среда")
        break
    elif number == 4:
        print("четверг")
        break
    elif number == 5:
        print("пятница")
        break
    elif number == 6:
        print("суббота")
        break
    elif number == 7:
        print("воскресенье")
        break
    else:
        print("Error. Try again")


